package net.rhysholloway.game.world.entity.util;

public class HitboxSpecs {

	public final int x, y, width, height, wOffset, hOffset;
	
	public HitboxSpecs(int x, int y, int width, int height, int wOffset, int hOffset) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
		this.wOffset = wOffset;
		this.hOffset = hOffset;
	}
	
}
